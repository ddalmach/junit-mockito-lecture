package io.javadev;

public final class MathUtils {

    public int add(int x, int y) {
        return x + y;
    }

    public int substract(int x, int y){
        return x -y;
    }

    public float divide(int x, int y) {
        return x / y;
    }

    public int multiply(int x, int y) {
        return x * y;
    }
}
